CREATE TABLE IMAGE
(
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  TITLE VARCHAR(255) NOT NULL,
  CAPTION VARCHAR(255) NOT NULL,
  DESCRIPTION VARCHAR(255) NOT NULL,
  HI_RES VARCHAR(5),
  CATEGORIES VARCHAR(255),
  TAGS VARCHAR(255),
  ALT_TEXT VARCHAR(255) NOT NULL,
  CREDIT VARCHAR(255) NOT NULL,
  COPYRIGHT VARCHAR(255) NOT NULL,
  SOURCE VARCHAR(255) NOT NULL,
  SERIES VARCHAR(255),
  SEASON VARCHAR(255),
  EPISODE VARCHAR(255),
  PUBLISHER_ID VARCHAR(255),
  CREATE_DATE VARCHAR(255),
  CREATE_TIME VARCHAR(255),
  FILE_NAME VARCHAR(50)
);

CREATE TABLE EPISODE
(
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  TITLE VARCHAR(255) NOT NULL,
  SUBHEAD VARCHAR(255) NOT NULL,
  SHORT_DESCRIPTION VARCHAR(500) NOT NULL,
  MEDIUM_DESCRIPTION VARCHAR(500),
  LONG_DESCRIPTION VARCHAR(2000),
  PRODUCTION_NUMBER INTEGER ,
  SERIES_REFERENCE VARCHAR(500) ,
  SEASON_REFERENCE VARCHAR(500),
  EPISODE_TYPE VARCHAR(500),
  TV_RATING VARCHAR(15),
  EPISODE_NUMBER VARCHAR(500),
  SUPPLEMENTARY_AIRING VARCHAR(500),
  CATEGORIES VARCHAR(255),
  TAGS VARCHAR(255),
  EXTERNAL_LINK VARCHAR(255)
);
CREATE TABLE SERIES
(
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  TITLE VARCHAR(255) NOT NULL,
  SUBHEAD VARCHAR(255) NOT NULL,
  SHORT_DESCRIPTION VARCHAR(500) NOT NULL,
  MEDIUM_DESCRIPTION VARCHAR(500),
  LONG_DESCRIPTION VARCHAR(1000),
  GENRE VARCHAR(255),
  RATING VARCHAR(30) NOT NULL,
  UNSCRIPTED BOOLEAN,
  PROGRAM_STATUS VARCHAR(255),
  SYNDICATED BOOLEAN,
  SERIES_TYPE VARCHAR(255) NOT NULL,
  RELATED_SERIES VARCHAR(255),
  SCHEDULED_DURATION VARCHAR(255),
  PROGRAMMING_TIMEFRAM VARCHAR(255) NOT NULL,
  CATEGORIES VARCHAR(255),
  TAGS VARCHAR(255),
  EXTERNAL_LINK VARCHAR(255)
);

CREATE TABLE ARTICLE
(
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  title VARCHAR(50) NOT NULL,
  body VARCHAR(500),
  image VARCHAR(200)
);

CREATE TABLE EVENT
(
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  TITLE VARCHAR(255) NOT NULL,
  SUBHEAD VARCHAR(255) NOT NULL,
  SHORT_DESCRIPTION VARCHAR(500) NOT NULL,
  MEDIUM_DESCRIPTION VARCHAR(500),
  LONG_DESCRIPTION VARCHAR(2000),
  GENRE VARCHAR(255),
  RATING VARCHAR(30) NOT NULL,
  UNSCRIPTED BOOLEAN,
  PROGRAM_STATUS VARCHAR(255),
  SYNDICATED BOOLEAN,
  EVENT_TYPE VARCHAR(255) NOT NULL,
  RELATED_SERIES VARCHAR(255),
  RELEASE_YEAR VARCHAR(255),
  AIR_TIMES VARCHAR(255),
  CATEGORIES VARCHAR(255),
  TAGS VARCHAR(255),
  EXTERNAL_LINK VARCHAR(255)
);

CREATE TABLE SEASON
(
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  TITLE VARCHAR(255) NOT NULL,
  SUBHEAD VARCHAR(255) NOT NULL,
  SHORT_DESCRIPTION VARCHAR(500) NOT NULL,
  MEDIUM_DESCRIPTION VARCHAR(500),
  LONG_DESCRIPTION VARCHAR(2000),
  PRODUCTION_NUMBER INTEGER ,
  SEASON_NUMBER INTEGER ,
  SEASON_DATE VARCHAR(500),
  CATEGORIES VARCHAR(255),
  TAGS VARCHAR(255),
  EXTERNAL_LINK VARCHAR(255)
);

